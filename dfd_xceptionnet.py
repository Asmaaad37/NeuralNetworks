def preprocess_frame(frame, target_size):
    frame = cv2.resize(frame, target_size)
    frame = cv2.cvtColor(frame, cv2.COLOR_BGR2RGB)
    frame = frame / 255.0  # normalize
    frame = np.expand_dims(frame, axis=0)
    return frame

